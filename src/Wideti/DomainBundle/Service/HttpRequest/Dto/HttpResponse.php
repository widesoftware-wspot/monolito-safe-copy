<?php

namespace Wideti\DomainBundle\Service\HttpRequest\Dto;

class HttpResponse
{
    private $status;
    private $content;

    public function __construct($status, $content)
    {
        $this->status = $status;
        $this->content = $content;
    }

    /**
     * @return mixed
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * @param mixed $status
     */
    public function setStatus($status)
    {
        $this->status = $status;
    }

    /**
     * @return mixed
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * @param mixed $content
     */
    public function setContent($content)
    {
        $this->content = $content;
    }


}